    private void btnAddEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddEmpActionPerformed
          if(validateInputs()==false)
          {
              JOptionPane.showMessageDialog(null, "Please fill all the values");
              return;
          }
          try
          {
          empid=txtEmpId.getText();
          empname=txtEmpName.getText();
          job=jcbJob.getSelectedItem().toString();
          sal=Double.parseDouble(txtSal.getText());
          EmpPojo ep=new EmpPojo();
          ep.setEmpid(empid);
          ep.setEmpname(empname);
          ep.setJob(job);
          ep.setSal(sal);
          boolean result=EmpDao.addEmployee(ep);
          if(result==true)
          {
              JOptionPane.showMessageDialog(null, "Record Add Successfully","Success!",JOptionPane.INFORMATION_MESSAGE);
              newEmpId();
          }
          else
          {
              JOptionPane.showMessageDialog(null, "Record not added!!","Error!",JOptionPane.ERROR_MESSAGE);
          }
          }
          catch(SQLException ex)
          {
              JOptionPane.showMessageDialog(null, "Problem in DB!");
              ex.printStackTrace();
          }
          catch(NumberFormatException ex)
          {
              JOptionPane.showMessageDialog(null, "Please enter digit for sal and eno!!");
              ex.printStackTrace();
          }
          
          
    }//GEN-LAST:event_btnAddEmpActionPerformed

