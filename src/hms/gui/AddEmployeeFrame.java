    private boolean validateInputs()
    {
        
        if(txtEmpName.getText().isEmpty()||txtSal.getText().isEmpty())
            return false;
        else if(jcbJob.getSelectedItem()=="~Select~")
            return false;
        return true;
        
    }
    private void newEmpId()
    {
       
        txtEmpName.setText("");
        txtSal.setText("");
        jcbJob.setSelectedIndex(0);
        try
        {
            txtEmpId.setText(EmpDao.getNewId());
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "Problem in DB!","Error!!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }
    private void closeFrame()
    {
        int response=JOptionPane.showConfirmDialog(null, "Are you want to Quit?", "Quiting!", JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
         if(response==JOptionPane.YES_OPTION)
         {
              DBConnection.closeConnection();
              System.exit(0);
         }
    }
    

