    private void showReceptionistData()
    {
        
        try
        {
            Object[] rows=new Object[4];
            ArrayList<UserDetailsPojo> list=ReceptionistDao.getAllReceptionist();
            DefaultTableModel dtm=(DefaultTableModel) jtReceptionistData.getModel();
            for(UserDetailsPojo e:list)
            {
                rows[0]=e.getUserid();
                rows[1]=e.getUsername();
                rows[2]=e.getEmpId();
                rows[3]=e.getUsertype();
                dtm.addRow(rows);
            }
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "Problem in Db!");
            ex.printStackTrace();
        }
    }

