    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            if(jcbDoctorId.getSelectedItem()=="~Select~")
            {
                JOptionPane.showMessageDialog(null, "Please Select Any Option!!");
                return;
            }
            boolean response=DoctorsDao.removeDoctor((String) jcbDoctorId.getSelectedItem());
            if(response)
            {
                JOptionPane.showMessageDialog(null, "Record Deleted Successfully!","Success",JOptionPane.INFORMATION_MESSAGE);
                loadDoctorId();
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Problem inDB!", "Error!!", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        
    }//GEN-LAST:event_btnDeleteActionPerformed

