    private void loadDoctorId() 
    {
        try
        {
            jcbDoctorId.removeAllItems();
            ArrayList<String> list=DoctorsDao.getAllDoctorId();
            Iterator itr=list.iterator();
            jcbDoctorId.addItem("~Select~");
            for(String str:list)
            {
                jcbDoctorId.addItem(str);
            }
            
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "Problem in Db!!","ERROR!!",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        
    }

