    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        if(validateInputs()==false)
        {
            JOptionPane.showMessageDialog(null, "Plese Fill the EmpId");
            work();
            return;
        }
        empid=txtEmpId.getText();
           try
           {
                 EmpPojo e=EmpDao.searchEmp(empid);
                 txtEmpName.setText(e.getEmpname());
                 txtJob1.setText(e.getJob());
                 txtSal.setText(String.valueOf(e.getSal())); 
           }
           catch(SQLException ex)
           {
               JOptionPane.showMessageDialog(null, "Problem in Db!","Error!",JOptionPane.ERROR_MESSAGE);
               ex.printStackTrace();
           }
    }//GEN-LAST:event_btnSearchActionPerformed

