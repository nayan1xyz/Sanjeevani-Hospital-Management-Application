    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int response=JOptionPane.showConfirmDialog(null, "Are you sure want to update?","Confirmation!",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
        if(response==JOptionPane.YES_OPTION)
        {
            try
            {
                EmpPojo e=new EmpPojo();
                e.setEmpid(txtEmpId.getText());
                e.setEmpname(txtEmpName.getText());
                e.setJob(txtJob1.getText());
                e.setSal(Double.parseDouble(txtSal.getText()));
                boolean result=EmpDao.updateEmployee(e);
                if(result==true)
                {
                    JOptionPane.showMessageDialog(null, "Record Successfully Updated!");
                    work();
                }
                else
                {
                    JOptionPane.showMessageDialog(null, "Record not update!!");
                }
            }
            catch(SQLException ex)
            {
                JOptionPane.showMessageDialog(null, "Problem in Db!");
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

