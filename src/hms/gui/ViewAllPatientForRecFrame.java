     private void loadPatientData()
    {
        
        try
        {
            Object[] rows=new Object[12];
            ArrayList<PatientPojo> list=PatientDao.getAllPatient();
            DefaultTableModel dtm=(DefaultTableModel) jtPatientData.getModel();
            for(PatientPojo e:list)
            {
                rows[0]=e.getP_id();
                rows[1]=e.getF_name();
                rows[2]=e.getS_name();
                rows[3]=e.getAge();
                rows[4]=e.getOpd();
                rows[5]=e.getGender();
                rows[6]=e.getM_status();
                rows[7]=e.getDate();
                rows[8]=e.getAddress();
                rows[9]=e.getCity();
                rows[10]=e.getMno();
                rows[11]=e.getDoctor_id();
                dtm.addRow(rows);
            }
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "Problem inDb!");
            ex.printStackTrace();
        }
    }

