    private void loadPatientId()
    {
        try
        {
           list=PatientDao.getPatientById();
           ArrayList<String> dlist=DoctorsDao.getAllDoctorId();
           
           for(String str:dlist)
           {
               jcbDoctorId.addItem(str);
           }
           Set<String> Pid=list.keySet();
          // jcbPatientId.addItem("~Select~");
           for(String str:Pid)
           {
               jcbPatientId.addItem(str);
           }
           
        }
        catch(SQLException ex)
        {
            JOptionPane.showMessageDialog(null, "Problem in DB!", "Error!!", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        
    }
    private boolean validateInputs()
    {
        f_name=txtFName.getText();
        doctor_id=(String)jcbDoctorId.getSelectedItem();
        s_name=txtSName.getText();
        age=Integer.parseInt(txtAge.getText());
        p_id=(String) jcbPatientId.getSelectedItem();
        opd=txtOpd.getText();
        gender=(String)jcbGender.getSelectedItem();
        m_status=(String)jcbMStatus.getSelectedItem();
        address=txtAddress.getText();
        city=txtCity.getText();
        mno=txtPhoneNo.getText();
        if(f_name.isEmpty()||s_name.isEmpty()||age==0||opd.isEmpty()||address.isEmpty()||city.isEmpty()||mno.isEmpty())
        {
            return false;
        }
        if(jcbPatientId.getSelectedItem()=="~Select~")
            return false;
        //if(jcbGender.getSelectedItem()=="~Select~"||jcbMStatus.getSelectedItem()=="~Select~"||jcbPatientId.getSelectedItem()=="~Select~"||jcbDoctorId.getSelectedItem()=="~Select~")
            //return false;
        return true;
    }

