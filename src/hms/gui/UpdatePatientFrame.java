    private void jcbPatientIdItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jcbPatientIdItemStateChanged
        if(jcbPatientId.getItemCount()==0)
            return;
        PatientPojo p=list.get(jcbPatientId.getSelectedItem());
        txtOpd.setText(p.getOpd());
        txtFName.setText(p.getF_name());
        txtSName.setText(p.getS_name());
        txtAge.setText(String.valueOf(p.getAge()));
        jcbDoctorId.setSelectedItem(p.getDoctor_id());
        jcbGender.setSelectedItem(p.getGender());
        jcbMStatus.setSelectedItem(p.getM_status());
        txtDate.setText(String.valueOf(p.getDate()));
        txtCity.setText(p.getCity());
        txtPhoneNo.setText(p.getMno());
        txtAddress.setText(p.getAddress());
    }//GEN-LAST:event_jcbPatientIdItemStateChanged

